package venus.riscv

/** Masks to get specific fields from RV32 instruction formats */
enum class InstructionFormat(val mask: Int, val shift: Int) {
    /* requires .toInt() as per KT-4749 */
    OPCODE              (0b00000000000000000000000001111111.toInt(), 0),
    RD                  (0b00000000000000000000111110000000.toInt(), 7),
    FUNCT3              (0b00000000000000000111000000000000.toInt(), 12),
    RS1                 (0b00000000000011111000000000000000.toInt(), 15),
    RS2                 (0b00000001111100000000000000000000.toInt(), 20),
    FUNCT7              (0b11111110000000000000000000000000.toInt(), 25),
    IMM_11_0            (0b11111111111100000000000000000000.toInt(), 20),
    IMM_4_0             (0b00000000000000000000111110000000.toInt(), 7),
    IMM_11_5            (0b11111110000000000000000000000000.toInt(), 25),
    IMM_11_B            (0b00000000000000000000000010000000.toInt(), 7),
    IMM_4_1             (0b00000000000000000000111100000000.toInt(), 8),
    IMM_31_12           (0b11111111111111111111000000000000.toInt(), 12),
    IMM_19_12           (0b00000000000011111111000000000000.toInt(), 12),
    IMM_11_J            (0b00000000000100000000000000000000.toInt(), 20),
    IMM_10_0            (0b01111111111000000000000000000000.toInt(), 21),
    IMM_20              (0b10000000000000000000000000000000.toInt(), 31),
}